import { cn } from "@/lib/utils";

export function IconButton({
  children, onClick, title, variant = "default", className,
}: {
  children: React.ReactNode;
  onClick: () => void;
  title?: string;
  variant?: "default" | "danger";
  className?: string;
}) {
  return (
    <button
      onClick={onClick}
      title={title}
      className={cn(
        "p-1.5 rounded-lg border transition-colors",
        variant === "danger"
          ? "text-gray-400 border-transparent hover:text-red-500 hover:bg-red-50 hover:border-red-100"
          : "text-gray-400 border-transparent hover:text-brand-600 hover:bg-brand-50 hover:border-brand-100",
        className
      )}
    >
      {children}
    </button>
  );
}

