import { cn } from "@/lib/utils";

type Variant = "default" | "danger" | "success";

const variantStyles: Record<Variant, string> = {
  default:
    "text-muted-foreground hover:text-primary hover:bg-primary/10 hover:border-primary/20",
  danger:
    "text-muted-foreground hover:text-destructive hover:bg-destructive/10 hover:border-destructive/20",
  success:
    "text-muted-foreground hover:text-success hover:bg-success/10 hover:border-success/20",
};

export function IconButton({
  children,
  onClick,
  title,
  variant = "default",
  className,
  disabled = false,
}: {
  children: React.ReactNode;
  onClick: () => void;
  title?: string;
  variant?: Variant;
  className?: string;
  disabled?: boolean;
}) {
  return (
    <button
      onClick={onClick}
      title={title}
      disabled={disabled}
      className={cn(
        "p-2 rounded-lg border border-transparent transition-all duration-200",
        "focus:outline-none focus:ring-2 focus:ring-ring focus:ring-offset-2 focus:ring-offset-background",
        "disabled:opacity-50 disabled:cursor-not-allowed",
        variantStyles[variant],
        className
      )}
    >
      {children}
    </button>
  );
}

